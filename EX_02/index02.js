/*Escreva um algoritmo para ler as dimensões de um retângulo (base e altura),
calcular e exibir a área do retângulo. */

let base = Number(window.prompt('Digite a Base do retângulo:'));
let altura = Number(window.prompt('Digite a Altura do retângulo:'));

let calculo = base * altura;

window.alert ('A área do retângulo é igual a: ' + calculo)